# Aspas tripas
'''
Se precisarmos exibir uma mensagem muito grande podemos utilizar as  aspas triplas  (‘’’ ou “””)
Exemplo:
'''
print("""Olá, tudo bem ? Meu nome é Higor e
eu tenho 21 anos de idade""")

print("\n")

mensagem = """Aprendi agora sobre aspas tripas, ela é usada para escrever mensagens muito longas no conteúdo das variáveis
e todas as quebras de linha serão exibidas do jeito que foram escritas. Parece ser algo bem necessário já que eu 
muitas vezes tenho dificuldades em quebrar linhas e fico me perguntando o tempo todo como eu deveria fazer isso,
então digo que aprender isso agora foi algo muito importante."""
print(mensagem)

print("\n")

# Corte/Pular linhas
'''
Às vezes precisamos pular a linha em nossa mensagem, a fim de facilitar o entendimento para o usuário e até mesmo
para nós desenvolvedores, desta forma o texto fica mais visível a todos.

O jeito certo de se fazer isso é colocar \n junta da palavra onde queremos quebrar ou cortar do tipo:
print("Hoje será um dia maravilho e não um dia triste \ncomo os anteriores")
'''
print("In the end, we will remember not the words of our enemies, \nbut the silence of our friends \nDr. Martin Luther King")